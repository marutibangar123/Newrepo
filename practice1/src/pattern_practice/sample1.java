package pattern_practice;

public class sample1
{
	
	//********
	//*******
	//******
	//*****
	public static void main(String[] args)
	{
		int star = 8;
		
		
		for(int i=1;i<=4;i++)
		{
		
			for(int k= 1;k<=star;k++)
			{
				System.out.print("*");
			}
			System.out.println();
			star--;
		}
		
	}
	
	
	

}
